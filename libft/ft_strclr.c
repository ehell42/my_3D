#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int j;

	j = 0;
	if (s != NULL)
	{
		i = ft_strlen(s);
		while (j <= i && s[j])
		{
			s[j] = '\0';
			j++;
		}
	}
}
