#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*a;

	i = ft_strlen(s1);
	a = (char*)malloc(sizeof(char) * (i + 1));
	if (a == NULL)
		return (NULL);
	ft_memcpy(a, s1, i);
	a[i] = '\0';
	return (a);
}
